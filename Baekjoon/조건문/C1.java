/* 두 정수 A와 B가 주어졌을 때, A와 B를 비교하는 프로그램을 작성하시오. (-10,000 ≤ A, B ≤ 10,000)
 * A가 B보다 큰 경우에는 '>'를 출력한다. A가 B보다 작은 경우에는 '<'를 출력한다. A와 B가 같은 경우에는 '=='를 출력한다.
 * 입력 : 1 2	-> 출력 : <	*/
import java.util.Scanner;

public class C1 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int a = sc.nextInt();
		int b = sc.nextInt();
		sc.close();
		
		if(a < b) {
			System.out.println("<");
		} else if(a > b) {
			System.out.println(">");
		} else {
			System.out.println("==");
		}
	}

}
