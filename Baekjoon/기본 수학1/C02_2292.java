/* 육각형으로 이루어진 벌집이 있다. 그림에서 보는 바와 같이 중앙의 방 1부터 시작해서 이웃하는 방에 돌아가면서 1씩 증가하는 번호를 주소로 매길 수 있다. 
 * 숫자 N이 주어졌을 때, 벌집의 중앙 1에서 N번 방까지 최소 개수의 방을 지나서 갈 때 몇 개의 방을 지나가는지(시작과 끝을 포함하여)를 계산하는 프로그램을 작성하시오. 
 * 예를 들면, 13까지는 3개, 58까지는 5개를 지난다. 첫째 줄에 N(1 ≤ N ≤ 1,000,000,000)이 주어진다.
 * 입력 : 13	-> 출력 : 3	
 * ps: 이해 잘 안됨	1 : 1, 2~7 : 2, 8~19 : 3, 20~37 : 4, 38~61 : 5	*/
import java.util.Scanner;

public class C02_2292 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int N = sc.nextInt();
		sc.close();
		
		int count = 1;	//1부터 세줌
		int range = 2;	//범위
		
		if(N == 1) {
			System.out.println(1);
		} else {	//N이 2이상인 수
			while(range <= N) {		// 범위가 N보다 커지기 직전까지 반복 
				range = range+(count*6);	// 다음 범위의 최솟값으로 초기화. 8 -> 20 -> 38 -> 62
				count++;
			}
			System.out.println(count);
		}
		
	}

}
